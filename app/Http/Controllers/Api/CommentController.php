    {
        try {
            $comment = Comment::create($request->validated());
            if($comment){
                return $this->sendResponse(__('message.insert.success',['name' => 'Comment']),CommentResource::make($comment));
            }else{
                return $this->sendError(__('message.insert.failure',['name'=>'Comment']));
            }
        } catch (\Throwable $th) {
            return $this->sendError($th->getMessage());
        }
    }

